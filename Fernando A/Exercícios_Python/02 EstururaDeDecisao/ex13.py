dia = int(input('Digite um número que corresponda a um dia da semana: '))

if dia == 1:
    print('1 - Domingo')

elif dia == 2:
    print('2 - Segunda-feira')

elif dia == 3:
    print('3 - Terça-feira')

elif dia == 4:
    print('4 - Quarta-feira')

elif dia == 5:
    print('5 - Quinta-feira')

elif dia == 6:
    print('6 - Sexta-feira')

elif dia == 7:
    print('7 - Sábado')

else:
    print("Valor inválido")